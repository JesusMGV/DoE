function keyLevels(
  values: Array<{ kind: "enemy" | "adventure"; value: 1 | 2 | 3 | 4; hardship?: HardshipKey | null }>
) {
  return {
    1: { encounter: { kind: values[0].kind, value: values[0].value }, hardship: values[0].hardship ?? null },
    2: { encounter: { kind: values[1].kind, value: values[1].value }, hardship: values[1].hardship ?? null },
    3: { encounter: { kind: values[2].kind, value: values[2].value }, hardship: values[2].hardship ?? null },
    4: { encounter: { kind: values[3].kind, value: values[3].value }, hardship: values[3].hardship ?? null },
  } as const;
}

